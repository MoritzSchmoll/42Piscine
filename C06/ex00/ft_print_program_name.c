#include <unistd.h>

int	main(int argv, char *args[])
{
	int	i;

	argv = 0;
	i = 0;
	while (args[0][i] != 0)
	{
		write(1, &args[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
